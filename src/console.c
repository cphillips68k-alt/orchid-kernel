/* Draw a single character at pixel position (px, py) */
static void draw_char(char c, uint64_t px, uint64_t py, uint32_t fg, uint32_t bg) {
    if (c < 32 || c > 126) c = '?';

    const unsigned char *glyph = font[c - 32];

    for (int row = 0; row < FONT_HEIGHT; row++) {
        unsigned char line = glyph[row];
        for (int col = 0; col < FONT_WIDTH; col++) {
            uint64_t x = px + col;
            uint64_t y = py + row;

            if (x >= fb_width || y >= fb_height) continue;

            uint32_t color = (line & (0x80 >> col)) ? fg : bg;
            fb_addr[y * fb_pixels_per_line + x] = color;
        }
    }
}

/* Scroll the screen up by one line */
static void scroll(void) {
    uint64_t line_bytes = fb_pitch;
    uint64_t font_line_bytes = FONT_HEIGHT * line_bytes;

    /* Move everything up one character row */
    unsigned char *video = (unsigned char *)fb_addr;
    for (uint64_t row = FONT_HEIGHT; row < fb_height; row++) {
        for (uint64_t byte = 0; byte < line_bytes; byte++) {
            video[(row - FONT_HEIGHT) * line_bytes + byte] = video[row * line_bytes + byte];
        }
    }

    /* Clear the last character row */
    for (uint64_t row = fb_height - FONT_HEIGHT; row < fb_height; row++) {
        for (uint64_t byte = 0; byte < line_bytes; byte++) {
            video[row * line_bytes + byte] = 0;
        }
    }
}

/* Advance cursor, scroll if needed */
static void advance_cursor(void) {
    cursor_x++;
    if (cursor_x * FONT_WIDTH + FONT_WIDTH > fb_width) {
        cursor_x = 0;
        cursor_y++;
    }
    if (cursor_y * FONT_HEIGHT + FONT_HEIGHT > fb_height) {
        cursor_y--;
        scroll();
    }
}

